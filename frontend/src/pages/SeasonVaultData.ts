export const getSeasonJerseys = (teamId: string, season: string) => {
  const seasonYear = parseInt(season, 10);
  const currentVault = CURRENT_TEAM_VAULT[teamId];

  if (currentVault && seasonYear >= 2017) {
    return currentVault.uniforms.map(uniform => ({
      ...uniform,
      sourceUrl: uniform.sourceUrl || getJerseyArchiveUrl(teamId, season),
    }));
  }

  // First, check if we have specific season data
  if (SEASON_JERSEYS[teamId] && SEASON_JERSEYS[teamId][season]) {
    return SEASON_JERSEYS[teamId][season];
  }

  // CHICAGO BULLS DYNASTY (1990-1998)
  if (teamId === '1610612741' && parseInt(season) >= 1990 && parseInt(season) <= 1998) {
    return [
      { label: 'Icon (Home)', color: '#FFFFFF', border: true },
      { label: 'Association (Road)', color: '#CE1141' },
      { label: 'Alternate (Pinstripe)', color: '#000000' }
    ];
  }

  // LAKERS SHOWTIME / KOBE ERA (1984-2010)
  if (teamId === '1610612747') {
    if (parseInt(season) >= 1984 && parseInt(season) < 2000) {
      return [
        { label: 'Showtime Home', color: '#FDB927' },
        { label: 'Showtime Road', color: '#552583' },
        { label: 'Sunday White', color: '#FFFFFF', border: true }
      ];
    }
    if (parseInt(season) >= 2000) {
      return [
        { label: 'Icon (Gold)', color: '#FDB927' },
        { label: 'Association (Purple)', color: '#552583' },
        { label: 'Statement (Black)', color: '#000000' },
        { label: 'City (Hollywood)', color: '#552583' }
      ];
    }
  }

  // CELTICS CLASSIC (All eras)
  if (teamId === '1610612738') {
    return [
      { label: 'Icon (Home)', color: '#FFFFFF', border: true },
      { label: 'Association (Road)', color: '#007A33' },
      { label: 'Alternate (Black)', color: '#000000' },
      { label: 'City (Banner)', color: '#007A33' }
    ];
  }

  // WARRIORS DYNASTY (2014-present)
  if (teamId === '1610612744' && parseInt(season) >= 2014) {
    return [
      { label: 'Icon (White)', color: '#FFFFFF', border: true },
      { label: 'Association (Blue)', color: '#1D428A' },
      { label: 'The Town (Yellow)', color: '#FFC72C' },
      { label: 'Classic (The Bay)', color: '#1D428A' }
    ];
  }

  // SPURS DYNASTY (1998-2014)
  if (teamId === '1610612759' && parseInt(season) >= 1998 && parseInt(season) <= 2014) {
    return [
      { label: 'Icon (Home)', color: '#FFFFFF', border: true },
      { label: 'Association (Road)', color: '#000000' },
      { label: 'Alternate (Silver)', color: '#C4CED4' }
    ];
  }

  // HEAT BIG THREE ERA (2010-2014)
  if (teamId === '1610612748' && parseInt(season) >= 2010 && parseInt(season) <= 2014) {
    return [
      { label: 'Icon (Home)', color: '#FFFFFF', border: true },
      { label: 'Association (Road)', color: '#98002E' },
      { label: 'Alternate (Black)', color: '#000000' }
    ];
  }

  // ROCKETS TWIN TOWERS ERA (1994-1998)
  if (teamId === '1610612745' && parseInt(season) >= 1994 && parseInt(season) <= 1998) {
    return [
      { label: 'Icon (Red)', color: '#CE1141' },
      { label: 'Association (Black)', color: '#000000' }
    ];
  }

  // MAVERICKS CHAMPIONSHIP ERA (2010-2011)
  if (teamId === '1610612742' && parseInt(season) === 2010) {
    return [
      { label: 'Icon (Home)', color: '#FFFFFF', border: true },
      { label: 'Association (Road)', color: '#00538C' }
    ];
  }

  // RAPTORS CHAMPIONSHIP ERA (2018-2019)
  if (teamId === '1610612761' && parseInt(season) >= 2018 && parseInt(season) <= 2019) {
    return [
      { label: 'Icon (Red)', color: '#CE1141' },
      { label: 'Association (Black)', color: '#000000' },
      { label: 'Statement (White)', color: '#FFFFFF', border: true }
    ];
  }

  // NUGGETS CHAMPIONSHIP ERA (2022-2023)
  if (teamId === '1610612743' && parseInt(season) >= 2022 && parseInt(season) <= 2023) {
    return [
      { label: 'Icon (Gold)', color: '#FEC524' },
      { label: 'Association (Blue)', color: '#0E2240' },
      { label: 'Statement (Black)', color: '#000000' }
    ];
  }

  // BUCKS CHAMPIONSHIP ERA (2020-2021)
  if (teamId === '1610612749' && parseInt(season) >= 2020 && parseInt(season) <= 2021) {
    return [
      { label: 'Icon (Green)', color: '#00471B' },
      { label: 'Association (White)', color: '#FFFFFF', border: true },
      { label: 'Statement (Black)', color: '#000000' }
    ];
  }

  // DEFAULT MODERN ERA JERSEYS (2015-present)
  if (parseInt(season) >= 2015) {
    return [
      { label: "Icon Edition", color: "#111827" },
      { label: "Association (Road)", color: "#FFFFFF", border: true },
      { label: "Statement (Alternate)", color: "#111827" },
      { label: "City Edition", color: "#666666" }
    ];
  }

  // DEFAULT LEGACY JERSEYS
  return [
    { label: "Icon Edition", color: "#111827" },
    { label: "Association", color: "#FFFFFF", border: true },
    { label: "Statement", color: "#111827" }
  ];
};

